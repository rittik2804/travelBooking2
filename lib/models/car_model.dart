class CarModel {
  int id;
  String name;
  String location;
  String seats;
  String picture;
  String price;
  String model;
  String color;
  int available;

  CarModel({
    required this.id,
    required this.name,
    required this.location,
    required this.seats,
    required this.picture,
    required this.price,
    required this.model,
    required this.color,
    required this.available,
  });
}
